public class Main {
    public static void main(String[] args) {
        int fn = 15;
        int sn = 10;
        int sum;
        int sub;
        int mult;
        int div;
        int remofdiv;

        sum = fn + sn;
        sub = fn - sn;
        mult = fn * sn;
        div = fn / sn;
        remofdiv = fn % sn;
        System.out.println("Sum " + sum);
        System.out.println("Sub " + sub);
        System.out.println("Mult " + mult);
        System.out.println("Div " + div);
        System.out.println("Remofdiv " + remofdiv);
    }
}